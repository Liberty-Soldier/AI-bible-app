export function normalizeBookName(book: string) {
  const trimmed = String(book || "").trim();

  if (!trimmed) return "";

  const compact = trimmed.replace(/\s+/g, "");

  return (
    bookAliasMap[trimmed] ||
    bookAliasMap[compact] ||
    bookAliasMap[trimmed.replace(/\./g, "")] ||
    trimmed
  );
}